    private void initCountryComboBox() {
        DefaultComboBoxModel<String> countryModel = new DefaultComboBoxModel<>();
        countryModel.addElement("Seleccione un país"); // Opción predeterminada
        countryModel.addElement("Argentina (+54)");
        countryModel.addElement("Bolivia (+591)");
        countryModel.addElement("Brasil (+55)");
        countryModel.addElement("Chile (+56)");
        countryModel.addElement("Colombia (+57)");
        countryModel.addElement("Ecuador (+593)");
        countryModel.addElement("Perú (+51)");
        countryModel.addElement("Uruguay (+598)");
        countryModel.addElement("Venezuela (+58)");

        PaisConboBox.setModel(countryModel);

        PaisConboBox.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    String selectedCountry = PaisConboBox.getSelectedItem().toString();
                    switch (selectedCountry) {
                        case "Argentina (+54)":
                            codigotxt.setText("+54");
                            break;
                        case "Bolivia (+591)":
                            codigotxt.setText("+591");
                            break;
                        case "Perú (+51)":
                            codigotxt.setText("+51");
                            break;
                        // Agrega más casos para otros países y códigos postales aquí
                        default:
                            codigotxt.setText(""); // Borra el código postal si no se selecciona un país válido
                            break;
                    }
                }
            }
        });
    }
    
    
