    private void EmpezarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EmpezarBtnActionPerformed
        // Obtener los datos ingresados en los campos de texto
        String nombre = NombreTxt.getText();
        String apellidos = ApellidosTxt.getText();
        String dni = DNItxt.getText();
        String email = EmailTxt.getText();
        String pais = PaisTxt.getText();
        String numeroCelular = NumeroCelularTxt.getText();
        String contrasena = new String(Contraseñatxt.getPassword()); // Contraseña como texto

        // Validar que los campos no estén vacíos (puedes agregar más validaciones según tus necesidades)
        if (nombre.isEmpty() || apellidos.isEmpty() || dni.isEmpty() || email.isEmpty() || pais.isEmpty() || numeroCelular.isEmpty() || contrasena.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Campos vacíos", JOptionPane.WARNING_MESSAGE);
            return;
        }

            // Guardar los datos en la base de datos
        try {
            CConexion conexionDB = new CConexion();
            Connection conexion = conexionDB.establecerConnection();

            // Verifica si la conexión se estableció correctamente
            if (conexion != null) {
                // Crear la consulta SQL para insertar un nuevo cliente en la tabla
                String insercionSQL = "INSERT INTO Cliente (Nombre, Apellido, Dni, Numero_Cel, Correo) VALUES (?, ?, ?, ?, ?)";

                // Crear una sentencia preparada
                PreparedStatement pstmt = conexion.prepareStatement(insercionSQL);
                pstmt.setString(1, nombre);
                pstmt.setString(2, apellidos);
                pstmt.setString(3, dni);
                pstmt.setString(4, numeroCelular);
                pstmt.setString(5, email);
                // Ejecutar la consulta para insertar los datos
                int filasInsertadas = pstmt.executeUpdate();

                // Cerrar la conexión
                pstmt.close();

                if (filasInsertadas > 0) {
                    JOptionPane.showMessageDialog(this, "Los datos se guardaron correctamente.", "Guardado exitoso", JOptionPane.INFORMATION_MESSAGE);
                    // Reiniciar o cerrar el formulario aquí, según tus necesidades
                    // Cierra la ventana actual
                    dispose();

                    // Abre la ventana de inicio de sesión (Login)
                    Login loginForm = new Login();
                    loginForm.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudieron guardar los datos.", "Error al guardar", JOptionPane.ERROR_MESSAGE);
                }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo establecer la conexión a la base de datos.", "Error de conexión", JOptionPane.ERROR_MESSAGE);
        }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al guardar los datos.", "Error SQL", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_EmpezarBtnActionPerformed

