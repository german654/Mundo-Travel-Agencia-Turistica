    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // Obtener datos del cliente desde los campos de texto
        String nombre = txt_nombre.getText();
        String apellido = txt_apellido.getText();
        String DNI = txt_DNI.getText();
        String celular = txt_celular.getText();
        String correo = txt_correo.getText();
        System.out.println(correo);
        // Crear un objeto Cliente
        Cliente cliente = new Cliente(nombre, apellido, Integer.parseInt(DNI), Integer.parseInt(celular), correo);

        
        // Obtener datos del pasaje desde los campos de texto
        String destino = cbx_Destino.getSelectedItem().toString();
        String fecha = txt_fecha.getText();
        String agencia = cbox_Agencia.getSelectedItem().toString();
        String codigoAsiento = txt_codigo.getText();
        
        // Crear un objeto Pasaje con los datos obtenidos
        Pasaje pasaje = new Pasaje(destino, fecha, agencia, codigoAsiento);

        // Ahora tienes el objeto Cliente y Pasaje listos para su procesamiento o almacenamiento

        // Puedes mostrar un mensaje de Ã©xito o realizar otras acciones aquÃ­
        // Por ejemplo, puedes imprimir los datos del cliente y pasaje
        System.out.println("Cliente: " + cliente.getNombre()+ " " + cliente.getApellido());
        System.out.println("Pasaje: " + pasaje.getAgencia()+ " " + pasaje.getCod_Vuelo());
    }//GEN-LAST:event_btn_guardarActionPerformed

